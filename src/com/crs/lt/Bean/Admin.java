package com.crs.lt.Bean;

public class Admin {

	int id;
	String name;
	int phone;
	String personal_mail;
	String collage_mail;
	int age;
	String username;
	String password;
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the phone
	 */
	public int getPhone() {
		return phone;
	}
	/**
	 * @param phone the phone to set
	 */
	public void setPhone(int phone) {
		this.phone = phone;
	}
	/**
	 * @return the personal_mail
	 */
	public String getPersonal_mail() {
		return personal_mail;
	}
	/**
	 * @param personal_mail the personal_mail to set
	 */
	public void setPersonal_mail(String personal_mail) {
		this.personal_mail = personal_mail;
	}
	/**
	 * @return the collage_mail
	 */
	public String getCollage_mail() {
		return collage_mail;
	}
	/**
	 * @param collage_mail the collage_mail to set
	 */
	public void setCollage_mail(String collage_mail) {
		this.collage_mail = collage_mail;
	}
	/**
	 * @return the age
	 */
	public int getAge() {
		return age;
	}
	/**
	 * @param age the age to set
	 */
	public void setAge(int age) {
		this.age = age;
	}
	/**
	 * @return the username
	 */
	public String getUsername() {
		return username;
	}
	/**
	 * @param username the username to set
	 */
	public void setUsername(String username) {
		this.username = username;
	}
	/**
	 * @return the password
	 */
	public String getPassword() {
		return password;
	}
	/**
	 * @param password the password to set
	 */
	public void setPassword(String password) {
		this.password = password;
	}
}
